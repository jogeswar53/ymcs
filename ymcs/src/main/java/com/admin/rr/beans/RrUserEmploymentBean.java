public class RrUserEmploymentBean {

	// Professional Info
	private String designation;
	private String organizationName;
	private String experienceCertificateImg;
	private String orgDateOfJoining;
	private String dateOfLeaving;

	/**
	 * @return the designation
	 */
	public String getDesignation() {
		return designation;
	}

	/**
	 * @param designation
	 *            the designation to set
	 */
	public void setDesignation(String designation) {
		this.designation = designation;
	}

	/**
	 * @return the organizationName
	 */
	public String getOrganizationName() {
		return organizationName;
	}

	/**
	 * @param organizationName
	 *            the organizationName to set
	 */
	public void setOrganizationName(String organizationName) {
		this.organizationName = organizationName;
	}

	/**
	 * @return the experienceCertificateImg
	 */
	public String getExperienceCertificateImg() {
		return experienceCertificateImg;
	}

	/**
	 * @param experienceCertificateImg
	 *            the experienceCertificateImg to set
	 */
	public void setExperienceCertificateImg(String experienceCertificateImg) {
		this.experienceCertificateImg = experienceCertificateImg;
	}

	/**
	 * @return the orgDateOfJoining
	 */
	public String getOrgDateOfJoining() {
		return orgDateOfJoining;
	}

	/**
	 * @param orgDateOfJoining
	 *            the orgDateOfJoining to set
	 */
	public void setOrgDateOfJoining(String orgDateOfJoining) {
		this.orgDateOfJoining = orgDateOfJoining;
	}

	/**
	 * @return the dateOfLeaving
	 */
	public String getDateOfLeaving() {
		return dateOfLeaving;
	}

	/**
	 * @param dateOfLeaving
	 *            the dateOfLeaving to set
	 */
	public void setDateOfLeaving(String dateOfLeaving) {
		this.dateOfLeaving = dateOfLeaving;
	}

}
